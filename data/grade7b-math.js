// 获取某章的所有知识点
function getChapterKnowledge(chapterId) {
  return knowledgeBase.chapters.find(ch => ch.chapterId === chapterId);
}

// 根据标签筛选知识点
function getKnowledgeByTag(tag) {
  const result = [];
  knowledgeBase.chapters.forEach(chapter => {
    chapter.knowledgePoints.forEach(kp => {
      if (kp.tags.includes(tag)) {
        result.push({
          chapterId: chapter.chapterId,
          chapterName: chapter.chapterName,
          ...kp
        });
      }
    });
  });
  return result;
}

// 搜索知识点
function searchKnowledge(keyword) {
  const result = [];
  knowledgeBase.chapters.forEach(chapter => {
    chapter.knowledgePoints.forEach(kp => {
      if (kp.name.includes(keyword) || kp.definition.includes(keyword)) {
        result.push({
          chapterId: chapter.chapterId,
          chapterName: chapter.chapterName,
          ...kp
        });
      }
    });
  });
  return result;
}
